def create_user(data):
    for each_item in data:
        User.objects.create(user_name=each_item['user_name'], user_profile_pic_url=each_item['user_profile_pic_url'])


def create_post(data):
    for each_item in data:
        user = User.objects.get(user_name=each_item['user_name'])

        Post.objects.create(post_description=each_item['post_description'], user=user)


def create_comment(data):
    for each_item in data:
        try:
            user = User.objects.get(user_name=each_item['user_name'])
            post = Post.objects.get(post_description=each_item['post_description'])
            Comment.objects.create(comment_description=each_item['comment_description'], post=post, user=user)
        except KeyError:
            pass


def create_reply(data):
    for each_item in data:
        if each_item['parent_comment']:
            user = User.objects.get(user_name=each_item['user_name'])
            comment = Comment.objects.get(comment_description=each_item['parent_comment'])
            post = comment.post
            Comment.objects.create(comment_description=each_item['comment_description'], post=post,
                                   parent_comment=comment, user=user)


def create_reaction_to_comment(data):
    for each_item in data:
        try:
            user = User.objects.get(user_name=each_item['user_name'])
            comment = Comment.objects.get(comment_description=each_item['comment_description'])
            Reaction.objects.create(reaction_type=each_item['reaction_type'], comment=comment, user=user)
        except KeyError:
            pass


def create_reaction_to_post(data):
    for each_item in data:
        try:
            user = User.objects.get(user_name=each_item['user_name'])
            post = Post.objects.get(post_description=each_item['post_description'])
            Reaction.objects.create(reaction_type=each_item['reaction_type'], post=post, user=user)
        except KeyError:
            pass


def populate(check_id):
    if check_id == 1:
        create_user(USER)
    elif check_id == 2:
        create_post(POST)
    elif check_id == 3:
        create_comment(COMMENT)
    elif check_id == 4:
        create_reply(REPLY)
    elif check_id == 5:
        create_reaction_to_post(POST_REACTION)
    elif check_id == 6:
        create_reaction_to_comment(COMMENT_REACTION)
    else:
        return 'Invalid Check_id'